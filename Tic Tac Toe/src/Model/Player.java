package Model;

public class Player {
    private String name = new String();
    private double scour ;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getScour() {
        return scour;
    }

    public void setScour(double scour) {
        this.scour = scour;
    }
}
